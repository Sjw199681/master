#转换成eval类型----转换成最相近的类型
a="123232323"
print(eval(a))
print(type(eval(a)))

#转换成int类型---整数类型
b='23232323'
print(int(b))
print(type(int(b)))

#转换成float类型---浮点类型
c="2.323232323"
print(float(c))
print(type(float(c)))
#转换成str类型---字符串类似
d='啦啦啦啦啦阿拉'
print(str(d))
print(type(str(d)))

#转换成list类型---列表类型
e="[这是一个列表]"
print(list(e))
print(type(list(e)))

#tuple 转换成一个元组
f="(123)"
print(tuple(f))
print(type(tuple(f)))




